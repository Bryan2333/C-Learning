//指针与数组的关系一
#include <stdio.h>

int main(void)
{
    int a[5] = {1, 2, 3, 4, 5};
    int *n;

    //  数组会隐形转换为第一个元素的地址
    if ((n = a) == (n = &a[0]))
    {
        printf("true\n");
    }

    // 通过指针间接访问数组
    // n == &a[0];
    // n+1 == &a[0] + 1;
    // n+2 == &a[0] + 2;

    // *n == a[0];
    // *(n+1) == a[1];
    // *(n+2) == a[2];

    // 
    return 0;
}